#209 - PROFUNDIZANDO EN SISTEMAS DE NUMERACION

a = 10
print(f'a = {a}')

#DE BINARIO A DECIMAL
a = 0b1010
print(f'a = {a}')

#SISTEMA OCTAL a DECIMAL
a = 0o12
print(f'a = {a}')

#HEXADECIMAL
a = 0xA
print(f'a = {a}')


#CLASE 210  CONVERTIR UN TIPO ENTERO, INCLUYENDO BASE

#BASE DECIMAL
a = int('23', 10)
print(f'a : {a}')

#BASE BINARIO
a = int('10111', 2)
print(f'a = {a}')

#BASE OCTAL
a = int('27', 8)
print(f'a = {a}')

#BASE HEXADECIMAL
a = int('17', 16)
print(f'a = {a}')

#BASE 5
a = int('34', 5)
